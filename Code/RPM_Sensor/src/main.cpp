/************************************************************
 *                       SETUP
 ************************************************************/
void setup() {

  Serial.begin(115200);
  delay(1000);

  otaSetup();

  pinMode(8,OUTPUT);
}

/************************************************************
 *                        LOOP
 ************************************************************/
void loop() {
  ArduinoOTA.handle();
  
  digitalWrite(8,HIGH);
  delay(500);

  digitalWrite(8,LOW);
  delay(500);
}